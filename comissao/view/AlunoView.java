private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
// TODO add your handling code here:
    AlunoController aluno = new AlunoController();

    
    String codigoSel = lblCodigo.getText();

    int var = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir ?", "Deseja realmente excluir ?", JOptionPane.OK_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);

    if (var == 0) {
        if (aluno.excluir(codigoSel)) {
            JOptionPane.showMessageDialog(this, "Operação realizada com Sucesso");
            carregaTabela();
        } else {
            JOptionPane.showMessageDialog(this, "Erro. Tente Novamente ou Contate o Administrador do Sistema", "Mensagem", JOptionPane.ERROR_MESSAGE);
        }


    }


}//GEN-LAST:event_btnExcluirActionPerformed

