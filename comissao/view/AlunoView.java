    private void carregaTabela(){
        Vector<String> vetColunas = new Vector<String>();
        vetColunas.addElement("Código");
        vetColunas.addElement("Aluno");
        vetColunas.addElement("E-mail");        
        vetColunas.addElement("Celular");                
        vetColunas.addElement("Saldo");                        
        DefaultTableModel objTableModel = new DefaultTableModel(vetColunas,0);
        objTableModel = AlunoController.getTableAlunos(objTableModel);
        jTable1.setModel(objTableModel);
    }    
    
