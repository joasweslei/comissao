private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
// TODO add your handling code here:
    String linhaSel = String.valueOf(jTable1.getSelectedRow());
    String codigoSel = String.valueOf(jTable1.getValueAt(Integer.parseInt(linhaSel), 0));
    String nomeSel = String.valueOf(jTable1.getValueAt(Integer.parseInt(linhaSel), 1));
    String emailSel = String.valueOf(jTable1.getValueAt(Integer.parseInt(linhaSel), 2));
    String celularSel = String.valueOf(jTable1.getValueAt(Integer.parseInt(linhaSel), 3));
    String saldoSel = String.valueOf(jTable1.getValueAt(Integer.parseInt(linhaSel), 4));

    lblCodigo.setText(codigoSel);
    txtNome.setText(nomeSel);
    txtEmail.setText(emailSel);
    txtCelular.setText(celularSel);
    lblSaldo.setText(saldoSel);

    if (modo != Modo.Inclusao) {
        JButton[] listaBotoes = {btnEditar};
        habilitaBotoes(listaBotoes, true);
    }


}//GEN-LAST:event_jTable1MouseClicked

