private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
    // TODO add your handling code here:
    ArrayList<String> dadosAluno = new ArrayList<String>();

    if (lblCodigo.getText() == "  ") {
        dadosAluno.add(null);
    } else {
        dadosAluno.add(lblCodigo.getText());
    }


    dadosAluno.add(txtNome.getText());
    dadosAluno.add(txtEmail.getText());
    dadosAluno.add(txtCelular.getText());

    AlunoController aluno = new AlunoController();

    if (aluno.salvar(dadosAluno)) {
        JOptionPane.showMessageDialog(this, "Operação realizada com Sucesso");
        carregaTabela();
    } else {
        JOptionPane.showMessageDialog(this, "Erro. Tente Novamente ou Contate o Administrador do Sistema", "Mensagem", JOptionPane.ERROR_MESSAGE);
    }

    JButton[] desativaBotoes = {btnSalvar, btnCancelar, btnEditar};
    habilitaBotoes(desativaBotoes, false);

    JButton[] ativaBotoes = {btnNovo, btnFechar, btnExcluir};
    habilitaBotoes(ativaBotoes, true);

    jTable1.setEnabled(true);
    modo = Modo.Navegacao;



}//GEN-LAST:event_btnSalvarActionPerformed

