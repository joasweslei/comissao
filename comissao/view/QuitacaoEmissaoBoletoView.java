private void carregaTabela(){
     Vector<String> vetColunas = new Vector<String>();
    vetColunas.addElement("Boleto"); 
    vetColunas.addElement("Aluno");
    vetColunas.addElement(" ");    
    vetColunas.addElement("Valor");
    vetColunas.addElement("Data de Emissão");
    vetColunas.addElement("Data de Quitação");
    vetColunas.addElement("Emitido");    
    objTableModel = new DefaultTableModel(vetColunas, 0);
    objTableModel = BoletoController.getBoletos(objTableModel);
    jTable1.setModel(objTableModel);
}    
    

private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
// TODO add your handling code here:
    
    lblMensagem.setText("Gerando boleto...");
    String linhaSel = String.valueOf(jTable1.getSelectedRow());

    BoletoController boleto = new BoletoController();

    ArrayList<String> dadosBoleto = new ArrayList<String>();

    ConfiguracaoController config = new ConfiguracaoController();


    dadosBoleto.add(config.getConfiguracao().getCedente());
    dadosBoleto.add(String.valueOf(jTable1.getValueAt(Integer.parseInt(linhaSel), 3)));
    dadosBoleto.add(String.valueOf(jTable1.getValueAt(Integer.parseInt(linhaSel), 2)));
    dadosBoleto.add(String.valueOf(jTable1.getValueAt(Integer.parseInt(linhaSel), 4)));
    dadosBoleto.add(Integer.toString(config.getConfiguracao().getBanco()));

    if (boleto.geraBoleto(dadosBoleto)) {
        String vIdBoleto = String.valueOf(jTable1.getValueAt(Integer.parseInt(linhaSel), 0));            
        boleto.setImpresso(vIdBoleto);
        String Msg = "Boleto gerado com sucesso!";
        JOptionPane.showMessageDialog(null, Msg, "Mensagem", JOptionPane.INFORMATION_MESSAGE);
    };
    


}//GEN-LAST:event_btnImprimirActionPerformed

