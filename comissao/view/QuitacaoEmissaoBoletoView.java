private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed
// TODO add your handling code here:
    String linhaSel = String.valueOf(jTable1.getSelectedRow());
    String vIdBoleto = String.valueOf(jTable1.getValueAt(Integer.parseInt(linhaSel), 0));    
    
    BoletoController boleto = new BoletoController();
    boleto.quitarBoleto(vIdBoleto);
    
    carregaTabela();
    
    String Msg = "Quitação do boleto registrada.";    
    JOptionPane.showMessageDialog(null, Msg, "Mensagem", JOptionPane.INFORMATION_MESSAGE);

    
    
}//GEN-LAST:event_btnQuitarActionPerformed

