private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
// TODO add your handling code here:
    ArrayList<String> dadosConfiguracao = new ArrayList<String>();



    dadosConfiguracao.add("1");
    dadosConfiguracao.add(spnNroMeses.getValue().toString());
    dadosConfiguracao.add(Integer.toString(cmbBanco.getSelectedIndex()));
    dadosConfiguracao.add(txtValor.getText());
    dadosConfiguracao.add(txtBoletosGerados.getText());
    dadosConfiguracao.add(txtCedente.getText());
    dadosConfiguracao.add(txtConta.getText());

    ConfiguracaoController config = new ConfiguracaoController();

    if (config.salvar(dadosConfiguracao)) {
        JOptionPane.showMessageDialog(this, "Operação realizada com Sucesso");
    } else {
        JOptionPane.showMessageDialog(this, "Erro. Tente Novamente ou Contate o Administrador do Sistema", "Mensagem", JOptionPane.ERROR_MESSAGE);
    }



}//GEN-LAST:event_btnSalvarActionPerformed

