private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
// TODO add your handling code here:


    ConfiguracaoController configuracao = new ConfiguracaoController();

    spnNroMeses.setValue(configuracao.getConfiguracao().getNroMeses());
    cmbBanco.setSelectedIndex(configuracao.getConfiguracao().getBanco());
    
    try {
        MaskFormatter maskFormat=new MaskFormatter("##.##");
        txtValor.setFormatterFactory(new DefaultFormatterFactory(maskFormat));
        txtValor.setText(Float.toString(configuracao.getConfiguracao().getValorMensalidade()));
    } catch (Exception e) {
            String errorMsg = "Erro ao formatar:  " + e.getMessage();
            JOptionPane.showMessageDialog(null, errorMsg, "Mensagem", JOptionPane.ERROR_MESSAGE);
    }
    
    txtBoletosGerados.setText(Integer.toString(configuracao.getConfiguracao().getNroBoletosGerados()));
    txtCedente.setText(configuracao.getConfiguracao().getCedente());
    txtConta.setText(configuracao.getConfiguracao().getConta());


}//GEN-LAST:event_formWindowActivated

